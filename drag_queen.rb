class DragQueen
    
    def initialize(name, age, hometown, style = "Pageant Queen")
        @name = name 
        @age = age
        @hometown = hometown
        @style = style
        
    end
    
    def name
        @name
    end
    
    def style
        @style
    end
    
    def age
        @age
    end
    
    def hometown
        @hometown
    end
end